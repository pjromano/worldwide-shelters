                <td align="left" valign="top" class="body_copy"><span class="header_small">Partnerships</span><br /><br />
                  Working in partnership with other leading aid organizations, Worldwide Shelters has developed a breakthrough, holistic approach to rebuilding communities ravaged by war and disaster. Within those partnerships, one specific role for Worldwide Shelters is to provide and implement high-quality transitional shelter as part of a secure, healthy environment that provides privacy and dignity for those who live there. To accomplish this, Worldwide Shelters has created uniquely adaptable shelters.<br /><br />
                  Not surprisingly, the Worldwide Shelters approach is finding a receptive and collaborative audience with many international aid agencies and governments. According to shelter experts Chuck Setchell of USAID/OFDA and Rod Imer, World Vision's Global Shelter Manager, "the first step in whatever we do in the emergency phase hasgot to be the first step to permanent housing."<br /><br />
                  International organizations including the UN High Commissioner for Refugees (UNHCR), International Organization for Migration (IOM/OIM) and the International Federation of Red Cross and Red Crescent Societies (IFRC) as well as private aid agencies such as World Vision, Save the Children, Doctors Without Borders/M&egrave;decins Sans Fronti&egrave;res (MSF), the International Rescue Committee (IRC), MedAir and Habitat for Humanity recognize the value of the Worldwide Shelters approach and have deployed Worldwide Shelters shelters in projects they have funded or conducted.<br /><br />
