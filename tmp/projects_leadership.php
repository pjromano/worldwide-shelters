                <td align="left" valign="top" class="body_copy"><p><span class="header_small">Thought Leadership <br />
                  in Transitional Shelters </span><br />
                  <br />
                  The Worldwide Shelters approach has found a receptive audience with many international aid agencies and governments. The Worldwide Shelters Transitional Shelter is a flexible shelter that can be a tent in the immediate wake of a disaster (when transport capacity to the affected region is at a premium) and later become the frame of a more permanent home and/or business place (as construction materials become available).  </p>
                  <p>This presents enormous advantages over traditional approaches.  As Mark Turner, a spokesman for the International Organization for Migration said in <em>The New York Times,</em> &ldquo;Tents really have a shelf life of not much more than six months... You can stand up in a shelter you build.  You can start a business there.&rdquo;  (Feb 4, 2010).  Worldwide Shelters will develop an ongoing exchange of ideas, designs and best practices for transitional shelter in order to maintain the positive momentum that has developed in the wake of the earthquake in Haiti. <br />
