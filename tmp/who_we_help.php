                <td align="left" valign="top" class="body_copy"><span class="header_small">The Scope of the Shelter Challenge</span>
                  <br />
                  <p>
                  Where do displaced populations go when they flee their homes and communities? Their options are extremely limited. Once they are forced to abandon their homes, many have nowhere to go; living where immediate shelter is found or, most often, having to build temporary homes from sparse or inadequate resources.</p>
                  <p>United Nations estimates put close to 1% of the world&rsquo;s 6.7 billion people currently displaced within their own countries. These more than 60 million people have been forced to flee their homes because of armed conflicts, other forms of violence, development projects, or natural disasters. The estimated number of people displaced by civil and military violence went over the 26 million mark in 2007, which was the highest global total since the early 1990s.</p>
                  <p>Displaced individuals and families are forced to flee without warning, leaving their possessions and land and becoming separated from their family and community. Millions suddenly find themselves destitute, with little or no access to basic services &mdash; food, water, shelter, a livelihood. Whatever the reason, displacement is often the beginning of an ordeal lasting years or even decades, characterized by suffering, discrimination, and a daily fight for survival.</p>
                  <p>These Internally Displaced People (IDPs) are often worse off than refugees because they haven&rsquo;t crossed an international border and they&rsquo;re not protected under international refugee law. Yet they may be persecuted by their own government and suffer from abuse, discrimination and neglect. Worldwide attention to their plight is growing but their numbers do not seem to diminish and living conditions for millions of them are deplorable. The UN Under-Secretary-General and Emergency Relief Coordinator calls the protection of IDPs &ldquo;the greatest gap in humanitarian response.&rdquo;</p>
                  <p>The plight of refugees is a huge problem as well. The UNHCR was working with 10.5 million refugees at the start of 2009. There are another 4.7 million registered refugees in 60 camps looked after by the United Nations Relief and Works Agency for Palestine Refugees in the Near East (UNRWA).</p>
                  <p>Finally, the number of people displaced by natural disasters can reach 50 million in a single year.</p>
